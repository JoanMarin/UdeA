public class Places {
    private String imagen;
    private String nombre_lugar;
    private String pequeña_descripcion;
    private double puntuacion;
    private String informacion_general;
    private String ubicacion;
    private double temperatura;
    private String sitios_recomendados;


    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public String getNombre_lugar() {
        return nombre_lugar;
    }

    public void setNombre_lugar(String nombre_lugar) {
        this.nombre_lugar = nombre_lugar;
    }

    public String getPequeña_descripcion() {
        return pequeña_descripcion;
    }

    public void setPequeña_descripcion(String pequeña_descripcion) {
        this.pequeña_descripcion = pequeña_descripcion;
    }

    public double getPuntuacion() {
        return puntuacion;
    }

    public void setPuntuacion(double puntuacion) {
        this.puntuacion = puntuacion;
    }

    public String getInformacion_general() {
        return informacion_general;
    }

    public void setInformacion_general(String informacion_general) {
        this.informacion_general = informacion_general;
    }

    public String getUbicacion() {
        return ubicacion;
    }

    public void setUbicacion(String ubicacion) {
        this.ubicacion = ubicacion;
    }

    public double getTemperatura() {
        return temperatura;
    }

    public void setTemperatura(double temperatura) {
        this.temperatura = temperatura;
    }

    public String getSitios_recomendados() {
        return sitios_recomendados;
    }

    public void setSitios_recomendados(String sitios_recomendados) {
        this.sitios_recomendados = sitios_recomendados;
    }
}
